package io.mandrel.client.content;

public class FieldFormatter {

	private String type;

	private byte[] value;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public byte[] getValue() {
		return value;
	}

	public void setValue(byte[] value) {
		this.value = value;
	}

}
